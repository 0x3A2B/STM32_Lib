  /* USER CODE BEGIN 2 */
	u8g2_i2c_handle_init(&hi2c1);
	u8g2_Setup_ssd1306_i2c_128x64_noname_f(&oled, U8G2_R0	, u8x8_byte_i2c_stm32_hal, u8x8_gpio_and_delay_stm32f4_hal);
	u8g2_InitDisplay(&oled);
	u8g2_SetPowerSave(&oled, 0);
